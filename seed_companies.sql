-- 1. Companies Table Schema (Rich Data)
DROP TABLE IF EXISTS companies;
CREATE TABLE IF NOT EXISTS companies (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  name TEXT NOT NULL, -- 기업명
  code TEXT, -- 종목코드 (상장사)
  biz_num TEXT UNIQUE, -- 사업자번호
  ceo TEXT, -- 대표자
  industry TEXT, -- 업종 (표준산업분류)
  founding_date DATE, -- 설립일
  address TEXT, -- 주소
  employee_count INTEGER, -- 직원수 (고용보험 기준)
  revenue INTEGER, -- 매출액 (백만원, DART 기준)
  profit INTEGER, -- 영업이익 (백만원)
  certifications TEXT, -- 보유인증 (콤마 구분)
  tags TEXT, -- 검색 태그 (채용 사이트 키워드 등)
  source TEXT DEFAULT 'INTEGRATED' -- 데이터 출처
);

